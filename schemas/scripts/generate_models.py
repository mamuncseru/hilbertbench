#!/usr/bin/env python3
"""
generate_models.py

Compiles schemas/v1.0/*.json → hilbertbench/models/v1_0/
Output is ALWAYS overwritten. Never edit generated files manually.

Usage:   python schemas/scripts/generate_models.py
Requires: pip install 'datamodel-code-generator[ruff]' 'pydantic>=2.6.0'

See INV-003: docs/architecture/001_invariants.md
"""

from __future__ import annotations

import importlib
import json
import shutil
import subprocess
import sys
from pathlib import Path

# ── Config ────────────────────────────────────────────────────────────────────

SCHEMA_DIR   = Path("schemas/v1.0")
OUTPUT_DIR   = Path("hilbertbench/models/v1_0")
REPO_ROOT    = Path(__file__).resolve().parents[2]

REQUIRED_SCHEMAS = ["trace.json", "span.json", "artifact.json", "catalog.json"]
EXPECTED_MODULES = ["trace.py", "span.py", "artifact.py", "catalog.py"]

FILE_HEADER = "\n".join([
    "# AUTO-GENERATED BY datamodel-codegen — DO NOT EDIT MANUALLY",
    "# Source: schemas/v1.0/",
    "# Regenerate: python schemas/scripts/generate_models.py",
    "# INV-003: Manual edits will be overwritten. Change the schema, not this file.",
])

ENUM_CHECKS = [
    ("trace",    "Status",      ["SEALED_SUCCESS", "SEALED_WITH_ERRORS", "CRASHED_IN_FLIGHT", "INITIALIZATION_FAILED"]),
    ("trace",    "Mode",        ["passive", "active"]),
    ("artifact", "Kind",        ["circuit_qasm", "pulse_schedule", "execution_outcome",
                                  "execution_error", "calibration_snapshot", "parameters",
                                  "observables", "generic_blob"]),
    ("artifact", "Encoding",    ["json", "ndjson", "parquet", "numpy_binary", "openqasm", "plaintext"]),
    ("artifact", "Compression", ["gzip", "zstd", "snappy"]),
    ("span",     "Status",      ["COMPLETED", "FAILED", "PARTIAL", "IN_FLIGHT"]),
]

# UPDATED: Removed V10 suffixes
ROOT_MODELS = [
    ("trace",    "HilbertbenchTraceManifest"),
    ("span",     "HilbertbenchSpan"),
    ("artifact", "HilbertbenchArtifactMetadata"),
    ("catalog",  "HilbertbenchArtifactCatalog"),
]

# ── Helpers ───────────────────────────────────────────────────────────────────

def ok(msg: str)   -> None: print(f"  ✓ {msg}")
def err(msg: str)  -> None: print(f"  ✗ {msg}", file=sys.stderr)
def warn(msg: str) -> None: print(f"  ⚠ {msg}")
def hdr(msg: str)  -> None: print(f"\n{msg}")

def die(msg: str, code: int = 1) -> None:
    print(f"\n❌ {msg}", file=sys.stderr)
    sys.exit(code)


# ── Step 1: Repo root guard ───────────────────────────────────────────────────

def check_repo_root() -> None:
    hdr("🔍 Checking working directory...")
    if not Path("schemas").is_dir() or not Path("hilbertbench/models").is_dir():
        die(
            "Run from repository root.\n"
            "  cd /path/to/hilbertbench && python schemas/scripts/generate_models.py"
        )
    ok("running from repo root")


# ── Step 2: Tool availability ─────────────────────────────────────────────────

def check_tools() -> list[str]:
    """Returns extra formatter flags for datamodel-codegen."""
    hdr("🔍 Checking required tools...")

    if not shutil.which("datamodel-codegen"):
        die(
            "datamodel-codegen not found.\n"
            "  pip install 'datamodel-code-generator[ruff]' 'pydantic>=2.6.0'"
        )
    ok("datamodel-codegen found")

    formatters: list[str] = []
    if shutil.which("ruff"):
        ok("ruff found — output will be formatted")
        formatters = ["--formatters", "ruff-format"]
    else:
        warn("ruff not found — install with: pip install 'datamodel-code-generator[ruff]'")

    return formatters


# ── Step 3: Schema presence ───────────────────────────────────────────────────

def check_schema_files() -> None:
    hdr("🔍 Checking schema files...")
    for name in REQUIRED_SCHEMAS:
        path = SCHEMA_DIR / name
        if not path.exists():
            die(f"Missing schema: {path}")
        ok(name)


# ── Step 4: JSON syntax + draft version ───────────────────────────────────────

def validate_json_and_draft() -> None:
    hdr("🔍 Validating JSON syntax and schema draft...")
    errors: list[str] = []
    for path in sorted(SCHEMA_DIR.glob("*.json")):
        try:
            data = json.loads(path.read_text())
            draft = data.get("$schema", "unknown")
            if "draft/2020-12" not in draft:
                errors.append(f"{path.name}: expected draft/2020-12, got: {draft}")
            else:
                ok(f"{path.name} (draft/2020-12)")
        except json.JSONDecodeError as exc:
            errors.append(f"{path.name}: {exc}")

    if errors:
        for e in errors:
            err(e)
        die("Schema validation failed.")


# ── Step 5: Cross-file $ref check ─────────────────────────────────────────────

def check_cross_refs() -> None:
    hdr("🔍 Checking cross-file $ref resolution...")
    catalog = json.loads((SCHEMA_DIR / "catalog.json").read_text())

    # catalog.json uses additionalProperties: { $ref: artifact.json }
    additional_props = (
        catalog.get("properties", {})
               .get("artifacts", {})
               .get("additionalProperties", {})
    )

    ref = additional_props.get("$ref", "")
    if "artifact.json" in ref:
        ok(f"catalog.json → artifact.json $ref found ({ref})")
        if (SCHEMA_DIR / "artifact.json").exists():
            ok("artifact.json exists — $ref is resolvable")
        else:
            die("artifact.json not found — $ref will break at generation time")
    else:
        warn("No $ref to artifact.json found in catalog.json — verify schema structure")


# ── Step 6: Clean output directory ────────────────────────────────────────────

def clean_output() -> None:
    hdr(f"🧹 Removing old {OUTPUT_DIR}/...")
    shutil.rmtree(OUTPUT_DIR, ignore_errors=True)
    OUTPUT_DIR.mkdir(parents=True)
    ok(f"{OUTPUT_DIR}/ recreated")


# ── Step 7: Generate Pydantic v2 models ───────────────────────────────────────

def run_codegen(formatters: list[str]) -> None:
    hdr("⚙️  Generating Pydantic v2 models...")
    cmd = [
        "datamodel-codegen",
        "--input",                 str(SCHEMA_DIR),
        "--input-file-type",       "jsonschema",
        "--output",                str(OUTPUT_DIR),
        "--output-model-type",     "pydantic_v2.BaseModel",
        "--target-python-version", "3.10",
        "--use-standard-collections",
        "--use-union-operator",
        "--strict-nullable",
        "--field-constraints",
        "--use-annotated",
        "--use-field-description",
        "--use-double-quotes",
        "--disable-timestamp",
        "--reuse-model",
        "--custom-file-header",    FILE_HEADER,
        *formatters,
    ]
    result = subprocess.run(cmd, capture_output=True, text=True)
    if result.returncode != 0:
        print(result.stdout)
        print(result.stderr, file=sys.stderr)
        die("datamodel-codegen failed.")
    ok("codegen completed")


# ── Step 8: Ensure __init__.py ────────────────────────────────────────────────

def ensure_init() -> None:
    init = OUTPUT_DIR / "__init__.py"
    if not init.exists():
        init.write_text("# AUTO-GENERATED PACKAGE — DO NOT EDIT\n")
        ok(f"created {init}")


# ── Step 9: Verify output files ───────────────────────────────────────────────

def verify_output_files() -> None:
    hdr("🔍 Checking output structure...")
    missing: list[str] = []
    for name in EXPECTED_MODULES:
        path = OUTPUT_DIR / name
        if path.exists():
            lines = path.read_text().count("\n")
            ok(f"{name} ({lines} lines)")
        else:
            err(f"{name} — NOT FOUND")
            missing.append(name)

    if missing:
        print(f"\n  Generated files were: {list(OUTPUT_DIR.iterdir())}")
        die("Expected output files are missing.")


# ── Step 10: Smoke test imports ───────────────────────────────────────────────

def _ensure_repo_on_path() -> None:
    repo = str(REPO_ROOT)
    if repo not in sys.path:
        sys.path.insert(0, repo)

def smoke_test_imports() -> None:
    hdr("🧪 Smoke testing package imports...")
    _ensure_repo_on_path()
    errors: list[str] = []

    for mod_name, expected_cls in ROOT_MODELS:
        full_mod = f"hilbertbench.models.v1_0.{mod_name}"
        try:
            mod = importlib.import_module(full_mod)
            all_classes = [
                x for x in dir(mod)
                if not x.startswith("_") and isinstance(getattr(mod, x), type)
            ]
            needle = expected_cls.lower().replace("_", "")
            matched = [c for c in all_classes if needle in c.lower().replace("_", "")]
            if matched:
                ok(f"{mod_name}.py → {matched[0]}")
            else:
                warn(f"{mod_name}.py imported OK but {expected_cls} not found; available: {all_classes}")
        except Exception as exc:
            errors.append(f"{mod_name}.py: {exc}")

    if errors:
        for e in errors:
            err(e)
        die("Import smoke test failed.")


# ── Step 11: Verify enum values ───────────────────────────────────────────────

def verify_enums() -> None:
    hdr("🔍 Verifying enum values against schemas...")
    _ensure_repo_on_path()
    all_ok = True

    for mod_name, enum_name, expected in ENUM_CHECKS:
        mod = importlib.import_module(f"hilbertbench.models.v1_0.{mod_name}")
        cls = getattr(mod, enum_name, None)
        if cls is None:
            warn(f"{mod_name}.{enum_name}: not found — skipping")
            continue
        actual = [e.value for e in cls]
        missing = [v for v in expected if v not in actual]
        if missing:
            err(f"{mod_name}.{enum_name}: missing values {missing}")
            all_ok = False
        else:
            ok(f"{mod_name}.{enum_name}: all {len(expected)} values present")

    if not all_ok:
        die("Enum verification failed.")


# ── Step 12: model_json_schema() roundtrip ────────────────────────────────────

def verify_schema_roundtrip() -> None:
    hdr("🔄 Checking model_json_schema() roundtrip...")
    _ensure_repo_on_path()

    for mod_name, cls_name in ROOT_MODELS:
        mod = importlib.import_module(f"hilbertbench.models.v1_0.{mod_name}")
        needle = cls_name.lower().replace("_", "")
        cls = next(
            (
                getattr(mod, x) for x in dir(mod)
                if needle in x.lower().replace("_", "")
                and isinstance(getattr(mod, x), type)
                and hasattr(getattr(mod, x), "model_json_schema")
            ),
            None,
        )
        if cls is None:
            warn(f"{cls_name} not found in {mod_name}.py — skipping")
            continue
        schema = cls.model_json_schema()
        assert "properties" in schema or "title" in schema, f"{cls_name}: empty schema"
        ok(f"{cls.__name__}.model_json_schema() OK")


# ── Main ──────────────────────────────────────────────────────────────────────

def main() -> None:
    check_repo_root()
    formatters = check_tools()
    check_schema_files()
    validate_json_and_draft()
    check_cross_refs()
    clean_output()
    run_codegen(formatters)
    ensure_init()
    verify_output_files()
    smoke_test_imports()
    verify_enums()
    verify_schema_roundtrip()

    print(f"\n✅ Models generated successfully in {OUTPUT_DIR}/")
    print()
    print("Next:")
    print("  1. Update hilbertbench/models/__init__.py with re-exports")
    print("  2. Run: pytest tests/compliance/test_schema_roundtrip.py")
    print("  3. Run: git add schemas/ hilbertbench/models/ && git commit")


if __name__ == "__main__":
    main()