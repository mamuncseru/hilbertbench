#!/usr/bin/env bash
set -euo pipefail

# =============================================================
# generate_python_models.sh
#
# Compiles schemas/v1.0/*.json → hilbertbench/models/v1_0/
# Output is ALWAYS overwritten. Never edit manually.
#
# Usage:    bash schemas/scripts/generate_python_models.sh
# Requires: pip install 'datamodel-code-generator[ruff]' 'pydantic>=2.6.0'
#
# See INV-003: docs/architecture/001_invariants.md
# =============================================================

SCHEMA_DIR="schemas/v1.0"
OUTPUT_DIR="hilbertbench/models/v1_0"
REQUIRED_SCHEMAS=("trace.json" "span.json" "artifact.json" "catalog.json")

HEADER=$'# AUTO-GENERATED BY datamodel-codegen — DO NOT EDIT MANUALLY\n# Source:     schemas/v1.0/\n# Regenerate: bash schemas/scripts/generate_python_models.sh\n# INV-003:    Manual edits will be overwritten. Change the schema, not this file.'

# ── 1. Guard: run from repo root ──────────────────────────────────────────
if [[ ! -d "schemas" || ! -d "hilbertbench/models" ]]; then
    echo "❌ Run from repository root."
    echo "   cd /path/to/hilbertbench && bash schemas/scripts/generate_python_models.sh"
    exit 1
fi

# ── 2. Guard: datamodel-codegen installed ─────────────────────────────────
if ! command -v datamodel-codegen &>/dev/null; then
    echo "❌ datamodel-codegen not found."
    echo "   pip install 'datamodel-code-generator[ruff]' 'pydantic>=2.6.0'"
    exit 1
fi

# ── 3. Formatter: use ruff if available ───────────────────────────────────
if command -v ruff &>/dev/null; then
    FORMATTERS="--formatters ruff-format"
    echo "✓ ruff detected — output will be formatted."
else
    FORMATTERS=""
    echo "⚠️  ruff not found — install with: pip install 'datamodel-code-generator[ruff]'"
fi

# ── 4. Guard: all 4 schemas present ───────────────────────────────────────
echo ""
echo "🔍 Checking schemas..."
for schema in "${REQUIRED_SCHEMAS[@]}"; do
    if [[ ! -f "${SCHEMA_DIR}/${schema}" ]]; then
        echo "   ❌ Missing: ${SCHEMA_DIR}/${schema}"
        exit 1
    fi
    echo "   ✓ ${schema}"
done

# ── 5. Validate JSON syntax + correct draft ───────────────────────────────
echo ""
echo "🔍 Validating JSON and schema draft..."
python3 -c "
import json, sys, pathlib

errors = []
for f in sorted(pathlib.Path('${SCHEMA_DIR}').glob('*.json')):
    try:
        data = json.load(open(f))
        draft = data.get('\$schema', 'unknown')
        if 'draft/2020-12' not in draft:
            errors.append(f'   ✗ {f.name}: expected draft/2020-12, got: {draft}')
        else:
            print(f'   ✓ {f.name}  (draft/2020-12)')
    except json.JSONDecodeError as e:
        errors.append(f'   ✗ {f.name}: {e}')

if errors:
    for e in errors: print(e)
    sys.exit(1)
"

# ── 6. Validate cross-file \$ref (catalog.json → artifact.json) ─────────────
echo ""
echo "🔍 Checking cross-file \$ref resolution..."
python3 -c "
import json, pathlib, sys

catalog = json.load(open('${SCHEMA_DIR}/catalog.json'))
artifacts_schema = catalog.get('properties', {}).get('artifacts', {})
pattern_props = artifacts_schema.get('patternProperties', {})

ref_found = False
for pattern, val in pattern_props.items():
    if '\$ref' in val and 'artifact.json' in val['\$ref']:
        ref_found = True
        print(f'   ✓ catalog.json references artifact.json via \$ref: {val[\"\$ref\"]}')
        # Confirm artifact.json actually exists at the referenced path
        artifact_path = pathlib.Path('${SCHEMA_DIR}/artifact.json')
        if artifact_path.exists():
            print(f'   ✓ artifact.json exists — \$ref is resolvable')
        else:
            print(f'   ✗ artifact.json not found — \$ref will break at generation time')
            sys.exit(1)

if not ref_found:
    print('   ⚠ No \$ref to artifact.json found in catalog.json — verify schema structure')
"

# ── 7. Clean previous output ──────────────────────────────────────────────
echo ""
echo "🧹 Removing old ${OUTPUT_DIR}/..."
rm -rf "${OUTPUT_DIR}"
mkdir -p "${OUTPUT_DIR}"

# ── 8. Generate Pydantic v2 models ────────────────────────────────────────
echo ""
echo "⚙️  Generating Pydantic v2 models..."

datamodel-codegen \
    --input                 "${SCHEMA_DIR}" \
    --input-file-type       jsonschema \
    --output                "${OUTPUT_DIR}" \
    --output-model-type     pydantic_v2.BaseModel \
    --target-python-version 3.10 \
    --use-standard-collections \
    --use-union-operator \
    --strict-nullable \
    --field-constraints \
    --use-annotated \
    --use-field-description \
    --use-double-quotes \
    --disable-timestamp \
    --reuse-model \
    --custom-file-header    "${HEADER}" \
    ${FORMATTERS}


# ── 9. Ensure __init__.py exists (generator may omit it) ──────────────────
if [[ ! -f "${OUTPUT_DIR}/__init__.py" ]]; then
    echo "# AUTO-GENERATED PACKAGE — DO NOT EDIT" > "${OUTPUT_DIR}/__init__.py"
    echo "   + created ${OUTPUT_DIR}/__init__.py"
fi

# ── 10. Verify expected output files ──────────────────────────────────────
echo ""
echo "🔍 Checking output structure..."
EXPECTED_MODULES=("trace.py" "span.py" "artifact.py" "catalog.py")
MISSING=()

for module in "${EXPECTED_MODULES[@]}"; do
    if [[ -f "${OUTPUT_DIR}/${module}" ]]; then
        SIZE=$(wc -l < "${OUTPUT_DIR}/${module}")
        echo "   ✓ ${module} (${SIZE} lines)"
    else
        MISSING+=("${module}")
        echo "   ✗ ${module} — NOT FOUND"
    fi
done

if [[ ${#MISSING[@]} -gt 0 ]]; then
    echo ""
    echo "   Generated files were:"
    ls -1 "${OUTPUT_DIR}/"
fi

# ── 11. Smoke test: import as package, not as isolated files ──────────────
# This is the fix for "attempted relative import with no known parent package"
# catalog.py uses relative imports because catalog.json has $ref → artifact.json
echo ""
echo "🧪 Smoke testing package imports..."
python3 -c "
import sys, importlib, pathlib

# Repo root must be on path so 'hilbertbench' resolves as a package
repo_root = str(pathlib.Path('${OUTPUT_DIR}').parents[2].resolve())
if repo_root not in sys.path:
    sys.path.insert(0, repo_root)

modules = [
    ('trace',    'HilbertbenchTraceManifest'),
    ('span',     'HilbertbenchSpanV10'),
    ('artifact', 'HilbertbenchArtifactMetadataV10'),
    ('catalog',  'HilbertbenchArtifactCatalogV10'),
]

errors = []
for mod_name, expected_class in modules:
    full_mod = f'hilbertbench.models.v1_0.{mod_name}'
    try:
        mod = importlib.import_module(full_mod)
        all_classes = [x for x in dir(mod) if not x.startswith('_') and isinstance(getattr(mod, x), type)]

        # Check expected class exists (tolerate minor naming variation)
        normalized_expected = expected_class.lower().replace('_', '')
        matched = [c for c in all_classes if normalized_expected in c.lower().replace('_', '')]

        if matched:
            print(f'   ✓ {mod_name}.py → {matched[0]}')
        else:
            print(f'   ⚠ {mod_name}.py imported OK but {expected_class} not found')
            print(f'     Available: {all_classes}')
    except Exception as e:
        errors.append(f'   ✗ {mod_name}.py: {e}')

if errors:
    for e in errors: print(e)
    sys.exit(1)
"


# ── 12. Verify enum values match schema definitions ───────────────────────
echo ""
echo "🔍 Verifying enum values against schemas..."
python3 -c "
import sys, importlib, pathlib

repo_root = str(pathlib.Path('${OUTPUT_DIR}').parents[2].resolve())
if repo_root not in sys.path:
    sys.path.insert(0, repo_root)

checks = [
    # (module, enum_class_name, expected_values)
    ('trace',    'Status', ['SEALED_SUCCESS', 'SEALED_WITH_ERRORS', 'CRASHED_IN_FLIGHT', 'INITIALIZATION_FAILED']),
    ('trace',    'Mode',   ['passive', 'active']),
    ('artifact', 'Kind',   ['circuit_qasm', 'pulse_schedule', 'execution_outcome',
                             'execution_error', 'calibration_snapshot', 'parameters',
                             'observables', 'generic_blob']),
    ('artifact', 'Encoding', ['json', 'ndjson', 'parquet', 'numpy_binary', 'openqasm', 'plaintext']),
    ('artifact', 'Compression', ['gzip', 'zstd', 'snappy']),
    ('span',     'Status', ['COMPLETED', 'FAILED', 'PARTIAL', 'IN_FLIGHT']),
]

all_ok = True
for mod_name, enum_name, expected in checks:
    mod = importlib.import_module(f'hilbertbench.models.v1_0.{mod_name}')
    cls = getattr(mod, enum_name, None)
    if cls is None:
        print(f'   ⚠ {mod_name}.{enum_name}: not found — skipping')
        continue
    actual = [e.value for e in cls]
    missing = [v for v in expected if v not in actual]
    if missing:
        print(f'   ✗ {mod_name}.{enum_name}: missing values {missing}')
        all_ok = False
    else:
        print(f'   ✓ {mod_name}.{enum_name}: all {len(expected)} values present')

if not all_ok:
    sys.exit(1)
"

# ── 13. Verify model_json_schema() roundtrip ──────────────────────────────
echo ""
echo "🔄 Checking model_json_schema() roundtrip..."
python3 -c "
import sys, importlib, pathlib

repo_root = str(pathlib.Path('${OUTPUT_DIR}').parents[2].resolve())
if repo_root not in sys.path:
    sys.path.insert(0, repo_root)

root_models = [
    ('trace',    'HilbertbenchTraceManifest'),
    ('span',     'HilbertbenchSpanV10'),
    ('artifact', 'HilbertbenchArtifactMetadataV10'),
    ('catalog',  'HilbertbenchArtifactCatalogV10'),
]

for mod_name, cls_name in root_models:
    mod = importlib.import_module(f'hilbertbench.models.v1_0.{mod_name}')
    # Tolerate naming variation
    normalized = cls_name.lower().replace('_', '')
    cls = next(
        (getattr(mod, x) for x in dir(mod)
         if normalized in x.lower().replace('_', '')
         and isinstance(getattr(mod, x), type)
         and hasattr(getattr(mod, x), 'model_json_schema')),
        None
    )
    if cls is None:
        print(f'   ⚠ {cls_name} not found in {mod_name}.py — skipping')
        continue
    schema = cls.model_json_schema()
    assert 'properties' in schema or 'title' in schema, f'{cls_name}: empty schema'
    print(f'   ✓ {cls.__name__}.model_json_schema() OK')
"

# ── Done ──────────────────────────────────────────────────────────────────
echo ""
echo "✅ Models generated successfully in ${OUTPUT_DIR}/"
echo ""
echo "Next:"
echo "  1. Update hilbertbench/models/__init__.py with re-exports"
echo "  2. Run: pytest tests/compliance/test_schema_roundtrip.py"
echo "  3. Run: git add schemas/ hilbertbench/models/ && git commit"
